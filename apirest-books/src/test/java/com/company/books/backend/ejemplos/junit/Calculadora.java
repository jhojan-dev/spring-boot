package com.company.books.backend.ejemplos.junit;

public class Calculadora {

    public float sum(float a, float b) {
        return a + b;
    }

    public int dividir(int a, int b) {
        return a / b;
    }

    public int multiplicar(int a, int b) {
        return a * b;
    }

    public int restar(int a, int b) {
        return a - b;
    }
}
